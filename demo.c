void msleep(int ms)
{
    struct timespec ts;
    ts.tv_sec = ms / 1000;
    ts.tv_nsec = (ms % 1000) * 1000000;
    nanosleep(&ts, NULL);
}

tSignal FBDgetProc(char type, tSignal index)
{
    time_t my_time;
    struct tm * timeinfo;
    //
    time(&my_time);
    timeinfo = localtime (&my_time);
#ifndef SILENT
    switch(type) {
    case 0:
        printf(" request InputPin(%ld)\n", index);
        // имитация RTC
        switch(index) {
            case GP_RTC_HOUR:
                return timeinfo->tm_hour;
            case GP_RTC_MINUTE:
                return timeinfo->tm_min;
            case GP_RTC_SECOND:
                return timeinfo->tm_sec;
            case GP_RTC_DAY:
                return timeinfo->tm_mday;
            case GP_RTC_MONTH:
                return timeinfo->tm_mon+1;
            case GP_RTC_YEAR:
                return timeinfo->tm_year+1900;
            default:
                return 0;
        }
    case 1:
        printf(" request NVRAM(%ld)\n", index);
        return 0;
    }
#endif
    return 0;
}

